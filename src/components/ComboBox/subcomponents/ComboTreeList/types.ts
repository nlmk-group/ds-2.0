import { IComboBoxTreeOption } from '../../types';

export interface IComboTreeListProps<T> {
  /**
   * Включает виртуализацию списка для более эффективного отображения большого количества элементов.
   * При включении отображаются только видимые в области просмотра элементы.
   */
  isVirtualize?: boolean;

  /**
   * Добавляет поле поиска для фильтрации элементов списка по их названию.
   */
  isSearch?: boolean;

  /**
   * Добавляет чекбокс "Выбрать все", позволяющий выбрать или снять выбор со всех элементов списка одновременно.
   */
  isCheckAll?: boolean;

  /**
   * Отображает индикатор загрузки при подгрузке данных.
   */
  isLoading?: boolean;

  /**
   * Определяет режим выбора: true - множественный выбор (чекбоксы), false - одиночный выбор.
   */
  isMultiple?: boolean;

  /**
   * Ограничивает максимальную глубину отображения дерева.
   * Если не указан, отображаются все уровни. Элементы на уровне maxLevel считаются листовыми.
   */
  maxLevel?: number;

  /**
   * Массив элементов древовидной структуры для отображения в списке.
   */
  items: T[];

  /**
   * Включает простой режим выбора чекбоксов, где выбор элементов происходит независимо
   * от их родительских и дочерних элементов. Когда false, выбор родителя выбирает всех детей,
   * и выбор всех детей выбирает родителя.
   */
  checkableSimple?: boolean;

  /**
   * Автоматический фокус на поиске при открытии выпадающего списка (по умолчанию false)
   */
  autoFocusSearch?: boolean;

  /**
   * Автоматическое раскрытие дерева элементов при поиске (по умолчанию false)
   */
  autoExpandOnSearch?: boolean;

  /**
   * Указывает, открыт ли dropdown (используется для автофокуса)
   */
  isDropdownOpen?: boolean;

  /**
   * Обработчик события изменения выбранных элементов.
   * Вызывается при выборе или снятии выбора с элементов списка.
   *
   * @param value - Массив выбранных элементов
   */
  onChange?: (value: IComboBoxTreeOption[]) => void;
}
